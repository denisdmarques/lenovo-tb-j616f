static void nt36523w_lcm_setbacklight_cmdq(void *handle, unsigned int level)
{
	unsigned int dbt = 0;

	if (level < 4 && level > 0)
		level = 4;

	LCM_LOGI("[nt36523w] %s, level = %d, bl_flag2 = %d\n", __func__, level, bl_flag2);

	dbt = ((level & 0xFF) << 4) | (level >> 4);

	if ((bl_flag2 == 0) && (level < 15)) {
		bl_flag2 = 1;
		bl_level1[2].para_list[0] = ((dbt >> 8) & 0xF);
		bl_level1[2].para_list[1] = (dbt & 0xFF);
		push_table(handle, bl_level1, ARRAY_SIZE(bl_level1), 1);
	} else {
		bl_level[2].para_list[0] = ((dbt >> 8) & 0xF);
		bl_level[2].para_list[1] = (dbt & 0xFF);
		push_table(handle, bl_level, ARRAY_SIZE(bl_level), 1);
	}

	if (level == 0)
		bl_flag2 = 0;
	else
		bl_flag2 = 1;
}


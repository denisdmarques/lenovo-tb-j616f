static void nt36523w_lcm_init(void)
{
	LCM_LOGI("[nt36523w] %s enter, islcmconnected = %d\n", __func__, islcmconnected);
	if (1 == islcmconnected) {
		SET_RESET_PIN(0);
		MDELAY(5);
		SET_RESET_PIN(1);
		MDELAY(5);
		SET_RESET_PIN(0);
		MDELAY(10);
		SET_RESET_PIN(1);
		MDELAY(10);

		push_table(NULL, init_setting_vdo, ARRAY_SIZE(init_setting_vdo), 1);
		MDELAY(50);
		// lcm_set_gpio_output(GPIO_LCD_BL_EN, GPIO_OUT_ONE); // Enable BL
		sgm37604a_write_byte(0x1A, 0x00);	// set bl 21.5mA
		sgm37604a_write_byte(0x19, 0xD7);	// set bl 21.5mA
		sgm37604a_write_byte(0x10, 0x1F);	// Enable BL
	}

	LCM_LOGI("[nt36523w] %s exit\n", __func__);
}

static void nt36523w_lcm_suspend(void)
{
	LCM_LOGI("[nt36523w] %s enter, islcmconnected = %d\n", __func__, islcmconnected);

	if (1 == islcmconnected) {
		// lcm_set_gpio_output(GPIO_LCD_BL_EN, GPIO_OUT_ZERO); // Disable BL
		sgm37604a_write_byte(0x10, 0x00);	// Disable BL
		sgm37604a_write_byte(0x1A, 0x00);
		sgm37604a_write_byte(0x19, 0x00);
		MDELAY(1);
		push_table(NULL, lcm_suspend_setting, ARRAY_SIZE(lcm_suspend_setting), 1);
	}

	LCM_LOGI("[nt36523w] %s exit\n", __func__);
}

